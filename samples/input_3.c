int main(void)
{
	int x;
	int y;
	int z;
	int a[3];
	{
		a[0] = 1;
		a[1] = 1;
		a[2] = 1;
		x = 3 * a[0] + 4 * a[1] + 5 * a[2];
		if (z == x)
		{
			while(x == z)
			{
				z = y * 2;
				x = z + 1;
			}
		}
		else
		{
			while(x != z)
			{
				x = a[0] * 2;
				a[0] = 2 * a[1] + 3 * y + 5 * (x + y + z);
			}
		}
	}
}

